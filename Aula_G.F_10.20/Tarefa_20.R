install.packages("FinancialMath")
library(FinancialMath)
invest<-500000
preco<-8
cv<-5.90
cf<-100000
deprec<-0.1*invest
residual<-c(0,0,0,0,0.5*invest)
rf<-0.1
rm<-0.12
beta<-2
icm<-0.18
ir<-0.34
taxa<-0.1
kd<-taxa*(1-ir)
ke<-rf+beta*(rm-rf)
we<-0.5
wd<-0.5
wacc<-(we*ke)+(wd*kd)
demanda<-c(200000,250000,300000,350000,400000)
receitab<-preco*demanda
icms<-receitab*icm
receital<-receitab-icms
custos<-cv*demanda+cf
lair<-receital-custos-deprec
irpj<-lair*ir
irpj[irpj<0]<-0
irpj
resultado<-lair-irpj
fc<-resultado+deprec+residual
tempo<-c(1,2,3,4,5)
NPV(invest,fc,tempo,wacc)

